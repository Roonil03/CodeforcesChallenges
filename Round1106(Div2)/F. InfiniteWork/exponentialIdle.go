func gcd(a, b int) int {
	for b != 0 {
		a, b = b, a%b
	}
	if a < 0 {
		return -a
	}
	return a
}

func lcm(a, b int) int {
	return a / gcd(a, b) * b
}

const MOD = 1000000007
const MAXN = 1000005

var fac [MAXN]int
var ifac [MAXN]int
var inv [MAXN]int

func precomp() {
	fac[0] = 1
	ifac[0] = 1
	inv[1] = 1
	for i := 1; i < MAXN; i++ {
		fac[i] = fac[i-1] * i % MOD
	}
	for i := 2; i < MAXN; i++ {
		inv[i] = (MOD - (MOD/i)*inv[MOD%i]%MOD) % MOD
	}
	for i := 1; i < MAXN; i++ {
		ifac[i] = ifac[i-1] * inv[i] % MOD
	}
}

func C(n, k int) int {
	if k < 0 || k > n {
		return 0
	}
	return fac[n] * ifac[k] % MOD * ifac[n-k] % MOD
}

func Cat(n int) int {
	if n < 0 {
		return 0
	}
	return C(2*n, n) * inv[n+1] % MOD
}

func solve() {
	m := in.rInt()
	n := in.rInt()
	n++
	cnt := 0
	ans := 1
	if m > 20 {
		m = 20
	}
	for i := m - 1; i >= 0; i-- {
		k := n >> i
		n -= k << i
		cnt += k
		ans = ans * ifac[k] % MOD
	}
	ans = ans * fac[cnt] % MOD
	cnt--
	ans = ans * Cat(cnt) % MOD
	fmt.Fprintln(out, ans)
}

func main() {
	out = bufio.NewWriterSize(os.Stdout, 1<<20)
	defer out.Flush()
	precomp()
	t := in.rInt()
	for t > 0 {
		t--
		solve()
	}
}
